nro1 = int(input("Ingrese el primer número: "))

nro2 = int(input("Ingrese el segundo número: "))

if nro1 == nro2:
    print(f"La suma de {nro1} + {nro2} = ", nro1+nro2)
elif nro1 > nro2:
    print (f"La resta de {nro1} - {nro2} = ", nro1-nro2)
else: print (f"El producto de {nro1} x {nro2} = ", nro1*nro2)

